char	*ft_strcapitalize(char *str)
{
	int i;

	i = 1;
	if (str[0] >= 'a' && str[0] <= 'z')
		str[0] = str[0] - 'a' + 'A';
	while (str[i] != '\0')
	{
		if ((str[i - 1] < '0' || (str[i - 1] > '9' && str[i - 1] < 'A') ||
			(str[i - 1] > 'Z' && str[i - 1] < 'a') ||
			str[i - 1] > 'z') && (str[i] >= 'a' && str[i] <= 'z'))
		{
			str[i] = str[i] - 'a' + 'A';
		}
		else if (((str[i - 1] >= '0' && str[i - 1] <= '9') ||
			(str[i - 1] >= 'A' && str[i - 1] <= 'Z') ||
			(str[i - 1] >= 'a' && str[i - 1] <= 'z')) &&
			(str[i] >= 'A' && str[i] <= 'Z'))
		{
			str[i] = str[i] -'A' + 'a';
		}
		i++;
	}
	return (str);
}
