unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	total;
	unsigned int	i;

	i = 0;
	total = 0;
	while (src[i] != '\0' && i < (size - 1))
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	while (src[total] != '\0')
		total++;
	return (total);
}
